    private void jLabelToolBarSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabelToolBarSaveMouseClicked
        // TODO add your handling code here:
        
        try{
            if(!jTextFieldName.getText().equals("")){
            // Estou criando um objeto do tipo (project)
            Project project =  new Project();
            project.setName(jTextFieldName.getText());
            project.setDescription(jTextAreaDescription.getText());
            // Estou mandando salva-lo no banco de dados
            controller.save(project);
            JOptionPane.showMessageDialog(rootPane, "Projeto Salvo com Sucesso!");
            this.dispose();
            } else {
                JOptionPane.showMessageDialog(rootPane, "O Projeto não foi salvo, pois o campo não foi preenchido!");
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
        // Ao clicar no Salvar irá fechar
        //this.dispose();      
    }//GEN-LAST:event_jLabelToolBarSaveMouseClicked

